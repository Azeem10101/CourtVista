function generateReviews(count, baseSeed) {
    const reviews = [];
    for (let i = 0; i < count; i++) {
        const idx = (baseSeed + i) % reviewTexts.length;
        const nameIdx = (baseSeed + i * 3) % reviewerNames.length;
        const stars = Math.min(5, Math.max(3, 4 + (((baseSeed + i) % 3) - 1)));
        const monthsAgo = ((baseSeed + i * 7) % 24) + 1;
        const d = new Date();
        d.setMonth(d.getMonth() - monthsAgo);
        reviews.push({
            id: `rev-${baseSeed}-${i}`,
            reviewer: reviewerNames[nameIdx],
            rating: stars,
            date: d.toISOString().split('T')[0],
            text: reviewTexts[idx],
            helpful: Math.floor(Math.random() * 20) + 1,
        });
    }
    return reviews;
}

